import type { Metadata } from "next";
import Link from "next/link";
import { ArrowLeft, CheckCircle2, Lock } from "lucide-react";
import { Footer } from "@/components/Footer";
import { Navbar } from "@/components/Navbar";
import { analyzePlan, summarizePlan } from "@/lib/plansight-ai/analysis";
import { demoPlan } from "@/lib/plansight-ai/demo";
import { createSharePayload } from "@/lib/plansight-ai/share";

type Props = {
  params: Promise<{ shareId: string }>;
};

export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const { shareId } = await params;

  return {
    title: `Shared Plan | ${shareId}`,
    description: "Read-only stakeholder view generated by PlanSight AI."
  };
}

export default async function SharePage({ params }: Props) {
  const { shareId } = await params;
  const share = createSharePayload(demoPlan);
  const metrics = summarizePlan(demoPlan);
  const insights = analyzePlan(demoPlan);

  return (
    <main className="min-h-screen bg-light">
      <section className="bg-dark text-white">
        <Navbar />
        <div className="mx-auto max-w-[1200px] px-6 py-16">
          <Link
            href="/products/plansight-ai"
            className="inline-flex items-center gap-2 text-sm font-medium text-slate-300 transition hover:text-white"
          >
            <ArrowLeft className="h-4 w-4" />
            Back to PlanSight AI
          </Link>

          <div className="mt-8 grid gap-8 lg:grid-cols-[1fr_0.8fr]">
            <div>
              <p className="text-sm font-semibold uppercase tracking-normal text-emerald-300">
                Stakeholder view
              </p>
              <h1 className="mt-3 text-[40px] font-bold leading-[1.12] tracking-normal md:text-[48px]">
                {share.title}
              </h1>
              <p className="mt-5 max-w-2xl text-lg leading-8 text-slate-300">
                This is a read-only plan share generated from a PM upload.
              </p>

              <div className="mt-8 flex flex-wrap gap-3 text-sm text-slate-200">
                <span className="inline-flex items-center gap-2 rounded-full bg-white/10 px-4 py-2">
                  <Lock className="h-4 w-4" />
                  Read only
                </span>
                <span className="inline-flex items-center gap-2 rounded-full bg-white/10 px-4 py-2">
                  {metrics.totalTasks} tasks
                </span>
                <span className="inline-flex items-center gap-2 rounded-full bg-white/10 px-4 py-2">
                  {metrics.completedTasks} complete
                </span>
              </div>
            </div>

            <div className="rounded-2xl border border-white/10 bg-white/[0.06] p-6 backdrop-blur">
              <p className="text-sm font-semibold uppercase tracking-normal text-slate-300">
                Share summary
              </p>
              <p className="mt-3 text-white">{share.summary}</p>
              <div className="mt-4 rounded-xl border border-white/10 bg-white/5 px-3 py-2 text-sm text-slate-200">
                Share ID: {shareId}
              </div>
              <div className="mt-4 rounded-xl border border-white/10 bg-white/5 px-3 py-2 text-sm text-slate-200">
                {share.shareUrl}
              </div>
            </div>
          </div>
        </div>
      </section>

      <section className="px-6 py-20">
        <div className="mx-auto grid max-w-[1200px] gap-6 lg:grid-cols-[1fr_0.9fr]">
          <div className="rounded-2xl border border-slate-200 bg-white p-8 shadow-soft">
            <h2 className="text-2xl font-semibold text-dark">Plan snapshot</h2>
            <div className="mt-6 space-y-4">
              {demoPlan.tasks.slice(0, 5).map((task) => (
                <div
                  key={task.id}
                  className="flex items-start justify-between gap-4 rounded-2xl bg-slate-50 p-4"
                >
                  <div>
                    <p className="font-medium text-dark">{task.name}</p>
                    <p className="mt-1 text-sm text-slate-500">
                      {task.resourceNames.length > 0 ? task.resourceNames.join(", ") : "Unassigned"}
                    </p>
                  </div>
                  <span className="text-sm font-semibold text-slate-700">
                    {task.percentComplete ?? 0}%
                  </span>
                </div>
              ))}
            </div>
          </div>

          <div className="rounded-2xl border border-slate-200 bg-white p-8 shadow-soft">
            <h2 className="text-2xl font-semibold text-dark">AI notes for stakeholders</h2>
            <div className="mt-6 space-y-4">
              {insights.map((insight) => (
                <div key={insight.title} className="flex items-start gap-3 rounded-2xl bg-slate-50 p-4">
                  <CheckCircle2 className="mt-0.5 h-5 w-5 shrink-0 text-primary" />
                  <div>
                    <p className="font-medium text-dark">{insight.title}</p>
                    <p className="mt-1 text-sm leading-6 text-slate-600">{insight.description}</p>
                  </div>
                </div>
              ))}
            </div>
          </div>
        </div>
      </section>

      <Footer />
    </main>
  );
}
